package Ejercicio2;

public class Parametros {
    public Parametros() {
    }

    public static void tipoParametro(){
        System.out.println("No ha pasado ningún parámetro");
    }

    public static void tipoParametro(char c){
        System.out.println("Ha pasado un caracter con valor " + c);
    }

    public static void tipoParametro(int i){
        System.out.println("Ha pasado un entero con valor " + i);
    }

    public static void tipoParametro(double d){
        System.out.println("Ha pasado un double con valor " + d);
    }

    public static void tipoParametro(float f){
        System.out.println("Ha pasado un float con valor " + f);
    }

    public static void tipoParametro(boolean b){
        System.out.println("Ha pasado un boolean con valor " + b);
    }

    public static void tipoParametro(byte by){
        System.out.println("Ha pasado un byte con valor " + by);
    }

    public static void tipoParametro(short s){
        System.out.println("Ha pasado un short con valor " + s);
    }

    public static void tipoParametro(long l){
        System.out.println("Ha pasado un long con valor " + l);
    }


}
